-- SECTION 3 — ABUSE SIGNALS TABLE
--
-- Stores automated abuse detection signals generated by detect_abuse_signals().
-- Written only by RPCs (SECURITY DEFINER). No user access via RLS.
-- Signals are reviewed by admins via the Abuse Detection dashboard page.
-- ══════════════════════════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS public.abuse_signals (
  id            UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id       UUID        NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,

  signal_type   TEXT        NOT NULL,
  -- 'credit_burn_rate' | 'failed_payments' | 'referral_farming' | 'manual'

  severity      TEXT        NOT NULL DEFAULT 'medium',
  -- 'low' | 'medium' | 'high' | 'critical'

  details       JSONB       NOT NULL DEFAULT '{}'::jsonb,
  -- Context: credits_consumed_24h, failed_count, referral_count, threshold, window_hours

  is_reviewed   BOOLEAN     NOT NULL DEFAULT FALSE,
  reviewed_by   UUID        REFERENCES auth.users(id) ON DELETE SET NULL,
  reviewed_at   TIMESTAMPTZ,
  review_action TEXT,
  -- 'cleared' | 'warned' | 'suspended'
  review_note   TEXT,

  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Indexes for fast admin queries
CREATE INDEX IF NOT EXISTS idx_abuse_signals_user_id
  ON public.abuse_signals(user_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_abuse_signals_pending
  ON public.abuse_signals(is_reviewed, created_at DESC)
  WHERE is_reviewed = FALSE;

CREATE INDEX IF NOT EXISTS idx_abuse_signals_type
  ON public.abuse_signals(signal_type, created_at DESC);

