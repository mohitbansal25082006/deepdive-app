-- SECTION 6 — RPC: get_abuse_signals_with_users()
--
-- Returns abuse signals joined with profile data (name, account_status).
-- Used by GET /api/admin/abuse after detection runs.
-- ══════════════════════════════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION public.get_abuse_signals_with_users(
  p_is_reviewed  BOOLEAN  DEFAULT FALSE,
  p_signal_type  TEXT     DEFAULT NULL,
  p_severity     TEXT     DEFAULT NULL,
  p_limit        INTEGER  DEFAULT 100
)
RETURNS TABLE (
  id             UUID,
  user_id        UUID,
  signal_type    TEXT,
  severity       TEXT,
  details        JSONB,
  is_reviewed    BOOLEAN,
  review_action  TEXT,
  review_note    TEXT,
  reviewed_at    TIMESTAMPTZ,
  created_at     TIMESTAMPTZ,
  full_name      TEXT,
  username       TEXT,
  account_status TEXT
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN QUERY
  SELECT
    s.id,
    s.user_id,
    s.signal_type,
    s.severity,
    s.details,
    s.is_reviewed,
    s.review_action,
    s.review_note,
    s.reviewed_at,
    s.created_at,
    p.full_name,
    p.username,
    p.account_status
  FROM public.abuse_signals s
  LEFT JOIN public.profiles p ON p.id = s.user_id
  WHERE
    (p_is_reviewed IS NULL OR s.is_reviewed  = p_is_reviewed)
    AND (p_signal_type IS NULL OR s.signal_type = p_signal_type)
    AND (p_severity    IS NULL OR s.severity    = p_severity)
  ORDER BY
    CASE s.severity
      WHEN 'critical' THEN 1
      WHEN 'high'     THEN 2
      WHEN 'medium'   THEN 3
      ELSE                 4
    END,
    s.created_at DESC
  LIMIT p_limit;
END;
$$;

-- ══════════════════════════════════════════════════════════════════════════════
-- SECTION 7 — RPC: resolve_abuse_signal()
--
-- Marks a signal as reviewed, records the admin's action and note,
-- and logs to admin_audit_log. Does NOT handle the credit deduction or
-- account status change — those are done by the existing admin_adjust_credits()
-- and admin_set_account_status() RPCs called from the same API route.
-- ══════════════════════════════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION public.resolve_abuse_signal(
  p_signal_id UUID,
  p_admin_id  UUID,
  p_action    TEXT,   -- 'cleared' | 'warned' | 'suspended'
  p_note      TEXT
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_signal RECORD;
BEGIN
  IF p_action NOT IN ('cleared', 'warned', 'suspended') THEN
    RAISE EXCEPTION 'Invalid review action: %. Must be cleared, warned, or suspended.', p_action;
  END IF;

  SELECT * INTO v_signal
  FROM public.abuse_signals
  WHERE id = p_signal_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Abuse signal not found: %', p_signal_id;
  END IF;

  UPDATE public.abuse_signals
  SET
    is_reviewed   = TRUE,
    reviewed_by   = p_admin_id,
    reviewed_at   = NOW(),
    review_action = p_action,
    review_note   = COALESCE(p_note, '')
  WHERE id = p_signal_id;

  INSERT INTO public.admin_audit_log
    (admin_user_id, target_user_id, action, resource_type, resource_id, after_value, reason)
  VALUES (
    p_admin_id,
    v_signal.user_id,
    'resolve_abuse_signal',
    'abuse_signal',
    p_signal_id::TEXT,
    jsonb_build_object(
      'signal_type',   v_signal.signal_type,
      'severity',      v_signal.severity,
      'review_action', p_action
    ),
    p_note
  );

  RETURN jsonb_build_object(
    'success',       true,
    'signal_id',     p_signal_id,
    'review_action', p_action,
    'user_id',       v_signal.user_id
  );
END;
$$;

-- ══════════════════════════════════════════════════════════════════════════════
-- SECTION 8 — RPC: get_credit_stats()
--
-- WHY:
--   PostgREST has a hardcoded 1000-row default limit on all SELECT queries.
--   If the platform has >1000 credit_transactions, per-type aggregation
--   (signup_bonus, referral_bonus, etc.) via the JS API is silently truncated,
--   causing stats like "Signup Bonuses" to show 0 or less than real values.
--
-- FIX:
--   This RPC runs a single SQL GROUP BY aggregation inside Postgres — no row
--   limit. Returns all credit stats as a single JSONB object in one call.
--   The credits/stats API route calls this RPC first and falls back to the
--   direct-query approach (with explicit .limit(500000)) only if the RPC fails.
-- ══════════════════════════════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION public.get_credit_stats()
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_total_issued     BIGINT  := 0;
  v_total_consumed   BIGINT  := 0;
  v_avg_balance      NUMERIC := 0;
  v_user_count       BIGINT  := 0;
  v_signup_bonuses   BIGINT  := 0;
  v_referral_bonuses BIGINT  := 0;
  v_admin_grants     BIGINT  := 0;
  v_purchased        BIGINT  := 0;
BEGIN
  -- ── Overall totals from user_credits (source of truth) ───────────────────────
  BEGIN
    SELECT
      COALESCE(SUM(total_purchased), 0)::BIGINT,
      COALESCE(SUM(total_consumed),  0)::BIGINT,
      COALESCE(AVG(balance),         0)::NUMERIC,
      COUNT(*)::BIGINT
    INTO v_total_issued, v_total_consumed, v_avg_balance, v_user_count
    FROM public.user_credits;
  EXCEPTION WHEN OTHERS THEN NULL;
  END;

  -- ── Per-type totals — single SQL aggregation, no PostgREST row limit ─────────
  BEGIN
    SELECT
      COALESCE(SUM(ABS(amount)) FILTER (WHERE type = 'signup_bonus'),                      0)::BIGINT,
      COALESCE(SUM(ABS(amount)) FILTER (WHERE type = 'referral_bonus'),                    0)::BIGINT,
      COALESCE(SUM(amount)      FILTER (WHERE type = 'admin_grant'    AND amount > 0),     0)::BIGINT,
      COALESCE(SUM(amount)      FILTER (WHERE type = 'purchase'       AND amount > 0),     0)::BIGINT
    INTO v_signup_bonuses, v_referral_bonuses, v_admin_grants, v_purchased
    FROM public.credit_transactions;
  EXCEPTION WHEN OTHERS THEN NULL;
  END;

  RETURN jsonb_build_object(
    'total_issued',            COALESCE(v_total_issued,     0),
    'total_consumed',          COALESCE(v_total_consumed,   0),
    'net_in_circulation',      GREATEST(0, COALESCE(v_total_issued, 0) - COALESCE(v_total_consumed, 0)),
    'avg_balance_per_user',    ROUND(COALESCE(v_avg_balance, 0)),
    'total_signup_bonuses',    COALESCE(v_signup_bonuses,   0),
    'total_purchased',         COALESCE(v_purchased,        0),
    'total_referral_bonuses',  COALESCE(v_referral_bonuses, 0),
    'total_admin_grants',      COALESCE(v_admin_grants,     0)
  );
END;
$$;

