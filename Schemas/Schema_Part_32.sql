-- ══════════════════════════════════════════════════════════════════════════════
-- SECTION 5 — RPC: detect_abuse_signals()
--
-- Scans the database for suspicious activity patterns and inserts new rows
-- into abuse_signals. Returns the count of NEW signals inserted.
-- Safe to call repeatedly — never creates duplicate signals within the window.
-- Called by GET /api/admin/abuse?runDetection=true when the page loads.
--
-- Detection rules:
--   1. credit_burn_rate  — >200 credits consumed by one user in 24 hours
--   2. failed_payments   — 3+ failed Razorpay orders for one user in 48 hours
--   3. referral_farming  — referral code used 5+ times in 24 hours (same referrer)
-- ══════════════════════════════════════════════════════════════════════════════

CREATE OR REPLACE FUNCTION public.detect_abuse_signals()
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_count INTEGER := 0;
  r       RECORD;
BEGIN

  -- ── Signal 1: High credit burn rate ─────────────────────────────────────────
  FOR r IN
    SELECT
      ct.user_id,
      SUM(ABS(ct.amount))::INTEGER AS total_consumed,
      COUNT(*)::INTEGER             AS tx_count
    FROM public.credit_transactions ct
    WHERE ct.type        = 'consume'
      AND ct.created_at >= NOW() - INTERVAL '24 hours'
    GROUP BY ct.user_id
    HAVING SUM(ABS(ct.amount)) > 200
  LOOP
    IF NOT EXISTS (
      SELECT 1 FROM public.abuse_signals
      WHERE user_id     = r.user_id
        AND signal_type = 'credit_burn_rate'
        AND created_at >= NOW() - INTERVAL '24 hours'
        AND is_reviewed = FALSE
    ) THEN
      INSERT INTO public.abuse_signals (user_id, signal_type, severity, details)
      VALUES (
        r.user_id,
        'credit_burn_rate',
        CASE
          WHEN r.total_consumed > 500 THEN 'critical'
          WHEN r.total_consumed > 350 THEN 'high'
          ELSE 'medium'
        END,
        jsonb_build_object(
          'credits_consumed_24h', r.total_consumed,
          'transaction_count',    r.tx_count,
          'window_hours',         24,
          'threshold',            200
        )
      );
      v_count := v_count + 1;
    END IF;
  END LOOP;

  -- ── Signal 2: Multiple failed payment attempts ────────────────────────────────
  BEGIN
    FOR r IN
      SELECT
        ro.user_id,
        COUNT(*)::INTEGER AS failed_count
      FROM public.razorpay_orders ro
      WHERE ro.status      = 'failed'
        AND ro.created_at >= NOW() - INTERVAL '48 hours'
      GROUP BY ro.user_id
      HAVING COUNT(*) >= 3
    LOOP
      IF NOT EXISTS (
        SELECT 1 FROM public.abuse_signals
        WHERE user_id     = r.user_id
          AND signal_type = 'failed_payments'
          AND created_at >= NOW() - INTERVAL '48 hours'
          AND is_reviewed = FALSE
      ) THEN
        INSERT INTO public.abuse_signals (user_id, signal_type, severity, details)
        VALUES (
          r.user_id,
          'failed_payments',
          CASE WHEN r.failed_count >= 6 THEN 'high' ELSE 'medium' END,
          jsonb_build_object(
            'failed_attempts', r.failed_count,
            'window_hours',    48,
            'threshold',       3
          )
        );
        v_count := v_count + 1;
      END IF;
    END LOOP;
  EXCEPTION WHEN OTHERS THEN
    NULL; -- razorpay_orders table may not exist in all environments
  END;

  -- ── Signal 3: Referral farming ────────────────────────────────────────────────
  BEGIN
    FOR r IN
      SELECT
        ru.referrer_id,
        COUNT(*)::INTEGER AS referral_count
      FROM public.referral_uses ru
      WHERE ru.created_at >= NOW() - INTERVAL '24 hours'
      GROUP BY ru.referrer_id
      HAVING COUNT(*) >= 5
    LOOP
      IF NOT EXISTS (
        SELECT 1 FROM public.abuse_signals
        WHERE user_id     = r.referrer_id
          AND signal_type = 'referral_farming'
          AND created_at >= NOW() - INTERVAL '24 hours'
          AND is_reviewed = FALSE
      ) THEN
        INSERT INTO public.abuse_signals (user_id, signal_type, severity, details)
        VALUES (
          r.referrer_id,
          'referral_farming',
          CASE WHEN r.referral_count >= 10 THEN 'critical' ELSE 'high' END,
          jsonb_build_object(
            'referrals_in_24h', r.referral_count,
            'window_hours',     24,
            'threshold',        5
          )
        );
        v_count := v_count + 1;
      END IF;
    END LOOP;
  EXCEPTION WHEN undefined_table THEN
    NULL; -- referral_uses may not exist in all deployments
  END;

  RETURN v_count;
END;
$$;

